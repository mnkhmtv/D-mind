                    meeting_id = len(existing_meetings) + 1  # –ù–æ–≤—ã–π ID = –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏—Ö + 1
                    
