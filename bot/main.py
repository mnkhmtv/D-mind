                    f"‚úÖ –í—Å—Ç—Ä–µ—á–∞ '{state['title']}' —Å–æ–∑–¥–∞–Ω–∞!\n\n"
                    f"üìÖ –î–∞—Ç–∞: {state['datetime']}\n"
