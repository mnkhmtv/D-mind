            f"üìù –û–ø–∏—Å–∞–Ω–∏–µ: {state['description'] or '–ù–µ —É–∫–∞–∑–∞–Ω–æ'}\n"
            f"üìÖ –î–∞—Ç–∞ –∏ –≤—Ä–µ–º—è: {state['datetime']}\n\n"
